# REEMPLAZA la configuración fija de DATABASES por esta lógica:
# Usa SQLite por defecto para que el proyecto arranque sin errores.
# Para usar MySQL (phpMyAdmin), exporta variables de entorno:
# DB_ENGINE=mysql, DB_NAME, DB_USER, DB_PASSWORD, DB_HOST, DB_PORT
